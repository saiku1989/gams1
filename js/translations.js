// 当前语言
let currentLanguage = 'zh';

// 获取翻译文本
function t(key) {
    return translations[currentLanguage][key] || key;
}

// 设置语言
function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        updatePageTranslations();
        localStorage.setItem('chillwork_language', lang);
    }
}

// 获取当前语言
function getCurrentLanguage() {
    return currentLanguage;
}

// 初始化语言
function initLanguage() {
    const savedLanguage = localStorage.getItem('chillwork_language');
    if (savedLanguage && translations[savedLanguage]) {
        currentLanguage = savedLanguage;
    }
    updatePageTranslations();
}

// 更新页面翻译
function updatePageTranslations() {
    // 更新所有带有 data-translate 属性的元素
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translatedText = t(key);
        element.textContent = translatedText;
    });
    
    // 更新占位符文本
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const translatedText = t(key);
        element.placeholder = translatedText;
    });
    
    // 更新标题属性
    document.querySelectorAll('[data-translate-title]').forEach(element => {
        const key = element.getAttribute('data-translate-title');
        const translatedText = t(key);
        element.title = translatedText;
    });
    
    // 更新语言选择框
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
    }
    
    // 更新HTML语言属性
    document.documentElement.lang = currentLanguage === 'zh' ? 'zh-CN' : 
                                   currentLanguage === 'en' ? 'en-US' : 'fr-FR';
}

// 获取所有支持的语言
function getSupportedLanguages() {
    return Object.keys(translations);
}

// 获取语言显示名称
function getLanguageDisplayName(lang) {
    const names = {
        'zh': '中文',
        'en': 'English',
        'fr': 'Français'
    };
    return names[lang] || lang;
}

